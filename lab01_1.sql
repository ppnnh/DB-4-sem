SELECT   ЗАКАЗЫ.Дата_поставки, ТОВАРЫ.Наименование
FROM      ТОВАРЫ INNER JOIN
                ЗАКАЗЫ ON ТОВАРЫ.Наименование = ЗАКАЗЫ.Наименование_товара INNER JOIN
                ЗАКАЗЧИКИ ON ЗАКАЗЫ.Заказчик = ЗАКАЗЧИКИ.Наименование_фирмы
WHERE   (ЗАКАЗЫ.Дата_поставки < CONVERT(DATETIME, '2021-02-21 00:00:00', 102))