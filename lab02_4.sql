SELECT опеондюбюрекх.тЮЛХКХЪ, опедлерш.мЮХЛЕМНБЮМХЕ
FROM опедлер_опеондюбюрекэ INNER JOIN
    опедлерш ON 
    опедлер_опеондюбюрекэ.йНД_ОПЕДЛЕРЮ = опедлерш.йНД_ОПЕДЛЕРЮ
     INNER JOIN
    опеондюбюрекх ON 
    опедлер_опеондюбюрекэ.йНД_ОПЕОНДЮБЮРЕКЪ = опеондюбюрекх.йНД_ОПЕОНДЮБЮРЕКЪ
WHERE (опеондюбюрекх.тЮЛХКХЪ = N'Patsei')